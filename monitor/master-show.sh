#!/bin/bash

me=`basename "$0"`
manifests_path="/etc/kubernetes/manifests/"

while [ 1 ]
do
    now=$(date +"%Y-%m-%d-%H:%M:%S")
    echo "=========================================================================="
    echo "$now"
    echo "--------------------------------------------------------------------------"

    ls "${manifests_path}/"
    echo "--------------------------------------------------------------------------"

    podman ps
    echo "----------------------------------------"
    crictl ps
    echo "--------------------------------------------------------------------------"

    echo "=========================================================================="
    sleep 1
done >$me.log.txt 2>&1
